/*
 Write a program to print the area of cylinder.
 */
public class cylinderArea {
    public static void main(String[] args){
        int radius = 3, height = 5;
        // double pi = 3.14;
        // Area = 2 * 3.14 * height * (radius + height)
        
        System.out.println("The area of cylinder is " + 2 * 3.14 * radius * (radius + height));
    }
}
